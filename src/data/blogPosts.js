export const categories = [...new Set(blogPosts.map(post => post.category))];

// Get featured posts
export const getFeaturedPosts = () => blogPosts.filter(post => post.featured);

// Get posts by category
export const getPostsByCategory = (category) =>
    blogPosts.filter(post => post.category === category);

// Get post by ID
export const getPostById = (id) => blogPosts.find(post => post.id === id);

// Get latest posts
export const getLatestPosts = (limit = 5) =>
    [...blogPosts].sort((a, b) => new Date(b.date) - new Date(a.date)).slice(0, limit);
